import fs from "node:fs";
import path from "node:path";
import vm from "node:vm";
import ts from "typescript";

const rootDir = process.cwd();
const dataFile = path.join(rootDir, "data", "questions.ts");
const seedFile = path.join(rootDir, "supabase", "question_bank_seed.sql");
const officialNotice =
  "This is an original practice question. It is not an official EPS-TOPIK question and is not copied from any official textbook or past exam.";

const mode = process.argv.includes("--validate") ? "validate" : "write";
const questionPool = loadQuestionPool();
const questionSets = buildQuestionSets(questionPool);
const validationIssues = validate(questionPool, questionSets);

if (validationIssues.length) {
  console.error("Question bank validation failed:");
  for (const issue of validationIssues) {
    console.error(`- ${issue}`);
  }
  process.exitCode = 1;
}

if (mode === "write") {
  fs.writeFileSync(seedFile, generateSeedSql(questionPool, questionSets), "utf8");
  console.log(`Wrote ${path.relative(rootDir, seedFile)}`);
}

function loadQuestionPool() {
  const source = fs.readFileSync(dataFile, "utf8");
  const compiled = ts.transpileModule(source, {
    compilerOptions: {
      esModuleInterop: true,
      module: ts.ModuleKind.CommonJS,
      target: ts.ScriptTarget.ES2020,
    },
    fileName: dataFile,
  }).outputText;

  const exports = {};
  const cjsModule = { exports };
  const sandbox = {
    exports,
    module: cjsModule,
    require: (specifier) => {
      if (specifier === "@/lib/questionUtils") {
        return {
          getChoiceByKey: () => undefined,
          getChoiceEntries: () => [],
          getCorrectChoiceKey: () => undefined,
        };
      }
      throw new Error(`Unexpected import in seed source: ${specifier}`);
    },
  };

  vm.runInNewContext(compiled, sandbox, { filename: dataFile });
  return cjsModule.exports.questionPool;
}

function buildQuestionSets(questions) {
  const sets = new Map();

  addExpectedSets(sets);

  for (const question of questions) {
    for (const setId of question.usage.levelTestSets) {
      addItem(sets, setId, question);
    }

    for (const setId of question.usage.dailyPracticeSets) {
      addItem(sets, setId, question);
    }

    if (question.usage.marketing) {
      addItem(sets, "marketingQuestions", question);
    }

    if (question.usage.replacementPool) {
      addItem(sets, "replacementPool", question);
    }
  }

  return sets;
}

function addExpectedSets(sets) {
  addSet(sets, "levelTestSetA", "Free Level Test Set A", "level_test", "Default public diagnostic level test.", true);
  addSet(sets, "levelTestSetB", "Free Level Test Set B", "level_test", "Prepared alternate level test set; not exposed in the UI yet.", false);

  for (let index = 1; index <= 20; index += 1) {
    const suffix = String(index).padStart(2, "0");
    addSet(
      sets,
      `dailySet${suffix}`,
      `Daily Practice Set ${suffix}`,
      "daily_practice",
      "Five-question daily practice set; data-only until the Daily Practice UI is implemented.",
      false,
    );
  }

  addSet(sets, "marketingQuestions", "Marketing Questions", "marketing", "Short practice questions for future marketing content.", false);
  addSet(sets, "replacementPool", "Replacement Pool", "replacement_pool", "Backup questions for future replacement and balancing workflows.", false);
}

function addSet(sets, id, name, type, purpose, isActive = false) {
  if (!sets.has(id)) {
    sets.set(id, { id, name, type, purpose, isActive, items: [] });
  }
}

function addItem(sets, setId, question) {
  if (!sets.has(setId)) {
    const type = setId.startsWith("dailySet") ? "daily_practice" : "level_test";
    addSet(sets, setId, setId, type, null, false);
  }
  sets.get(setId).items.push(question);
}

function generateSeedSql(questions, sets) {
  const managedSetIds = [...sets.keys()];
  const lines = [
    "-- Generated by scripts/question-bank-seed.mjs from data/questions.ts.",
    "-- Run supabase/question_bank_schema.sql before this seed file.",
    "begin;",
    "",
    "insert into public.questions (",
    "  id, section, context, difficulty, tags, question, options, answer,",
    "  explanation_en, explanation_ne, source_type, reference_scope, official_notice, is_active",
    ") values",
    questions.map(questionValueSql).join(",\n"),
    "on conflict (id) do update set",
    "  section = excluded.section,",
    "  context = excluded.context,",
    "  difficulty = excluded.difficulty,",
    "  tags = excluded.tags,",
    "  question = excluded.question,",
    "  options = excluded.options,",
    "  answer = excluded.answer,",
    "  explanation_en = excluded.explanation_en,",
    "  explanation_ne = excluded.explanation_ne,",
    "  source_type = excluded.source_type,",
    "  reference_scope = excluded.reference_scope,",
    "  official_notice = excluded.official_notice,",
    "  is_active = excluded.is_active;",
    "",
    "insert into public.question_sets (id, name, type, purpose, is_active) values",
    [...sets.values()].map(questionSetValueSql).join(",\n"),
    "on conflict (id) do update set",
    "  name = excluded.name,",
    "  type = excluded.type,",
    "  purpose = excluded.purpose,",
    "  is_active = excluded.is_active;",
    "",
    `delete from public.question_set_items where set_id in (${managedSetIds
      .map(sqlString)
      .join(", ")});`,
    "",
  ];

  const items = [...sets.values()].flatMap((set) =>
    set.items.map((question, index) => ({
      setId: set.id,
      questionId: question.id,
      orderIndex: index,
    })),
  );

  if (items.length) {
    lines.push(
      "insert into public.question_set_items (set_id, question_id, order_index) values",
      items.map(questionSetItemValueSql).join(",\n"),
      "on conflict (set_id, question_id) do update set",
      "  order_index = excluded.order_index;",
      "",
    );
  }

  lines.push("commit;", "");
  return lines.join("\n");
}

function questionValueSql(question) {
  return `(${[
    sqlString(question.id),
    sqlString(question.section),
    sqlString(question.context),
    sqlString(question.difficulty),
    sqlTextArray(question.tags),
    sqlString(question.question),
    `${sqlString(JSON.stringify(question.options))}::jsonb`,
    sqlString(question.answer),
    sqlString(question.explanationEn),
    sqlString(question.explanationNe),
    sqlString(question.sourceType),
    sqlString(question.referenceScope),
    sqlString(question.officialNotice),
    question.usage.isActive ? "true" : "false",
  ].join(", ")})`;
}

function questionSetValueSql(set) {
  return `(${[
    sqlString(set.id),
    sqlString(set.name),
    sqlString(set.type),
    set.purpose ? sqlString(set.purpose) : "null",
    set.isActive ? "true" : "false",
  ].join(", ")})`;
}

function questionSetItemValueSql(item) {
  return `(${[
    sqlString(item.setId),
    sqlString(item.questionId),
    item.orderIndex,
  ].join(", ")})`;
}

function validate(questions, sets) {
  const issues = [];
  const ids = new Set();
  const dangerousPhrases = [
    "official question",
    "real exam",
    "past question",
    "copied from official",
    "leaked",
    "secret",
    "guaranteed pass",
    "pass guarantee",
    "100% success",
    "hrd korea official",
    "eps korea official",
    "eps nepal official",
  ];

  for (const question of questions) {
    if (ids.has(question.id)) issues.push(`${question.id}: duplicate id`);
    ids.add(question.id);

    if (question.sourceType !== "original") {
      issues.push(`${question.id}: sourceType must be original`);
    }
    if (question.officialNotice !== officialNotice) {
      issues.push(`${question.id}: officialNotice is not standardized`);
    }
    if (!question.referenceScope.trim()) {
      issues.push(`${question.id}: referenceScope is empty`);
    }
    if (!question.explanationEn.trim()) {
      issues.push(`${question.id}: explanationEn is empty`);
    }
    if (!question.explanationNe.trim()) {
      issues.push(`${question.id}: explanationNe is empty`);
    }
    if (!question.question.trim()) {
      issues.push(`${question.id}: question is empty`);
    }
    if (!question.answer.trim()) {
      issues.push(`${question.id}: answer is empty`);
    }
    if (question.options.length !== 4) {
      issues.push(`${question.id}: options must contain exactly 4 choices`);
    }
    if (question.options.some((option) => typeof option !== "string" || !option.trim())) {
      issues.push(`${question.id}: every option must be a non-empty string`);
    }
    if (!question.options.includes(question.answer)) {
      issues.push(`${question.id}: answer is not one of the options`);
    }

    const text = [
      question.question,
      ...question.options,
      question.answer,
      question.explanationEn,
      question.explanationNe,
      question.referenceScope,
    ].join(" ");

    if (/\b(placeholder|tbd|todo|coming soon)\b/i.test(text)) {
      issues.push(`${question.id}: contains placeholder text`);
    }

    const lowerText = text.toLowerCase();
    const dangerousPhrase = dangerousPhrases.find((phrase) =>
      lowerText.includes(phrase),
    );
    if (dangerousPhrase) {
      issues.push(`${question.id}: contains restricted wording "${dangerousPhrase}"`);
    }
  }

  validateLevelTestA(sets.get("levelTestSetA")?.items ?? [], issues);
  validateOptionalLevelTestB(sets.get("levelTestSetB")?.items ?? [], issues);
  validateDailySets(sets, issues);

  return issues;
}

function validateLevelTestA(questions, issues) {
  if (questions.length !== 20) {
    issues.push(`levelTestSetA: expected 20 questions, found ${questions.length}`);
  }
  validateCounts("levelTestSetA sections", countBy(questions, "section"), {
    Vocabulary: 5,
    Grammar: 5,
    Reading: 5,
    "Workplace Korean": 5,
    "Mixed Review": 0,
  }, issues);
  validateCounts("levelTestSetA difficulty", countBy(questions, "difficulty"), {
    easy: 8,
    medium: 10,
    hard: 2,
  }, issues);
}

function validateOptionalLevelTestB(questions, issues) {
  if (!questions.length) return;

  if (questions.length !== 20) {
    issues.push(`levelTestSetB: expected 20 questions, found ${questions.length}`);
  }
  validateCounts("levelTestSetB sections", countBy(questions, "section"), {
    Vocabulary: 5,
    Grammar: 5,
    Reading: 5,
    "Workplace Korean": 5,
    "Mixed Review": 0,
  }, issues);
  validateCounts("levelTestSetB difficulty", countBy(questions, "difficulty"), {
    easy: 7,
    medium: 11,
    hard: 2,
  }, issues);
}

function validateDailySets(sets, issues) {
  for (const [setId, set] of sets) {
    if (!/^dailySet\d{2}$/.test(setId) || !set.items.length) continue;
    if (set.items.length !== 5) {
      issues.push(`${setId}: expected 5 questions, found ${set.items.length}`);
    }
    validateCounts(`${setId} sections`, countBy(set.items, "section"), {
      Vocabulary: 1,
      Grammar: 1,
      Reading: 1,
      "Workplace Korean": 1,
      "Mixed Review": 1,
    }, issues);
  }
}

function validateCounts(label, actual, expected, issues) {
  for (const [key, expectedCount] of Object.entries(expected)) {
    if ((actual[key] ?? 0) !== expectedCount) {
      issues.push(`${label}: expected ${key}=${expectedCount}, found ${actual[key] ?? 0}`);
    }
  }
}

function countBy(items, key) {
  return items.reduce((counts, item) => {
    counts[item[key]] = (counts[item[key]] ?? 0) + 1;
    return counts;
  }, {});
}

function sqlTextArray(values) {
  if (!values.length) return "'{}'::text[]";
  return `array[${values.map(sqlString).join(", ")}]::text[]`;
}

function sqlString(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}
